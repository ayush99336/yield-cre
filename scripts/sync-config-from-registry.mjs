#!/usr/bin/env node

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')

const defaultTestnetRegistryPath = fs.existsSync(path.join(root, 'deployments', 'addresses.testnet.crosschain.json'))
  ? path.join(root, 'deployments', 'addresses.testnet.crosschain.json')
  : path.join(root, 'deployments', 'addresses.testnet.json')

const testnetRegistryPath = process.env.TESTNET_ADDRESS_REGISTRY_PATH ?? defaultTestnetRegistryPath
const mainnetRegistryPath =
  process.env.MAINNET_ADDRESS_REGISTRY_PATH ?? path.join(root, 'deployments', 'addresses.mainnet.json')

const readJson = (filePath) => {
  if (!fs.existsSync(filePath)) return null
  return JSON.parse(fs.readFileSync(filePath, 'utf8'))
}

const testnetRegistry =
  readJson(testnetRegistryPath) ??
  readJson(path.join(root, 'deployments', 'addresses.testnet.example.json'))
const mainnetRegistry =
  readJson(mainnetRegistryPath) ??
  readJson(path.join(root, 'deployments', 'addresses.mainnet.example.json'))

if (!testnetRegistry) {
  throw new Error(`Missing testnet registry at ${testnetRegistryPath}`)
}

const chains = Object.values(testnetRegistry.destinations ?? {}).map((destination) => ({
  id: destination.id,
  chainName: destination.chainName,
  usdc: destination.usdc,
  dataProvider: destination.dataProvider,
  pool: destination.pool,
  receiver: destination.receiver,
  enabled: destination.enabled !== false,
}))

const creConfig = {
  schedule: process.env.CRE_SCHEDULE ?? '*/10 * * * *',
  rebalanceThresholdBps: Number(process.env.REBALANCE_THRESHOLD_BPS ?? '50'),
  cooldownSeconds: Number(process.env.COOLDOWN_SECONDS ?? '1800'),
  homeChainName:
    process.env.CRE_HOME_CHAIN_NAME ??
    (testnetRegistry.home?.chainName?.includes('sepolia') ? 'ethereum-testnet-sepolia' : 'ethereum-testnet-sepolia'),
  homeVaultAddress: testnetRegistry.home?.vault ?? '0x1111111111111111111111111111111111111111',
  homeVaultWriteGasLimit: process.env.CRE_HOME_VAULT_WRITE_GAS_LIMIT ?? '500000',
  currentYieldChainFallback: process.env.CRE_CURRENT_YIELD_CHAIN_FALLBACK ?? chains[0]?.id ?? '',
  writeEnabled: process.env.CRE_WRITE_ENABLED === 'true',
  chains,
}

const creConfigPath = path.join(root, 'services', 'cre-workflow', 'cre', 'omni-yield', 'config.json')
fs.writeFileSync(creConfigPath, `${JSON.stringify(creConfig, null, 2)}\n`, 'utf8')

const generatedEnv = [
  `# generated by scripts/sync-config-from-registry.mjs`,
  `TESTNET_HOME_CHAIN_MODE=${process.env.TESTNET_HOME_CHAIN_MODE ?? process.env.HOME_CHAIN_MODE ?? 'world'}`,
  `TESTNET_HOME_VAULT_ADDRESS=${testnetRegistry.home?.vault ?? ''}`,
  `TESTNET_HOME_USDC=${testnetRegistry.home?.usdc ?? ''}`,
  `TESTNET_HOME_RPC_URL=${testnetRegistry.home?.rpcUrl ?? ''}`,
  `TESTNET_WORLD_RPC_URL=${testnetRegistry.home?.rpcUrl ?? ''}`,
  `NEXT_PUBLIC_TESTNET_HOME_CHAIN_MODE=${process.env.NEXT_PUBLIC_TESTNET_HOME_CHAIN_MODE ?? process.env.TESTNET_HOME_CHAIN_MODE ?? process.env.HOME_CHAIN_MODE ?? 'world'}`,
  `NEXT_PUBLIC_TESTNET_HOME_VAULT_ADDRESS=${testnetRegistry.home?.vault ?? ''}`,
  `NEXT_PUBLIC_TESTNET_HOME_USDC_ADDRESS=${testnetRegistry.home?.usdc ?? ''}`,
  `NEXT_PUBLIC_TESTNET_HOME_RPC_URL=${testnetRegistry.home?.rpcUrl ?? ''}`,
  `NEXT_PUBLIC_TESTNET_WORLD_RPC_URL=${testnetRegistry.home?.rpcUrl ?? ''}`,
  `MAINNET_HOME_VAULT_ADDRESS=${mainnetRegistry?.home?.vault ?? ''}`,
  `MAINNET_HOME_USDC=${mainnetRegistry?.home?.usdc ?? ''}`,
  `MAINNET_WORLD_RPC_URL=${mainnetRegistry?.home?.rpcUrl ?? ''}`,
  `NEXT_PUBLIC_MAINNET_HOME_VAULT_ADDRESS=${mainnetRegistry?.home?.vault ?? ''}`,
  `NEXT_PUBLIC_MAINNET_HOME_USDC_ADDRESS=${mainnetRegistry?.home?.usdc ?? ''}`,
  `NEXT_PUBLIC_MAINNET_WORLD_RPC_URL=${mainnetRegistry?.home?.rpcUrl ?? ''}`,
]
const generatedEnvPath = path.join(root, '.env.registry.generated')
fs.writeFileSync(generatedEnvPath, `${generatedEnv.join('\n')}\n`, 'utf8')

console.log(`CRE config synced -> ${creConfigPath}`)
console.log(`Generated env exports -> ${generatedEnvPath}`)
